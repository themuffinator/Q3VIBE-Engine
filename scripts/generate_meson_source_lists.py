#!/usr/bin/env python3
from __future__ import annotations

import os
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "build-support" / "meson" / "sources" / "meson.build"
OUT_BASE = OUT.parent
ENGINE_ROOT = "src/engine"


GROUPS = [
    ("botlib_sources", f"{ENGINE_ROOT}/botlib", ["*.cpp"], []),
    ("client_sources", f"{ENGINE_ROOT}/client", ["*.cpp"], ["cl_curl.cpp"]),
    ("client_curl_sources", f"{ENGINE_ROOT}/client", ["cl_curl.cpp"], []),
    ("qcommon_sources", f"{ENGINE_ROOT}/qcommon", ["*.cpp"], ["vm_aarch64.cpp", "vm_armv7l.cpp", "vm_powerpc.cpp", "vm_x86.cpp"]),
    ("qcommon_vm_x86_sources", f"{ENGINE_ROOT}/qcommon", ["vm_x86.cpp"], []),
    ("qcommon_vm_aarch64_sources", f"{ENGINE_ROOT}/qcommon", ["vm_aarch64.cpp"], []),
    ("qcommon_vm_armv7l_sources", f"{ENGINE_ROOT}/qcommon", ["vm_armv7l.cpp"], []),
    ("qcommon_vm_powerpc_sources", f"{ENGINE_ROOT}/qcommon", ["vm_powerpc.cpp"], []),
    ("renderer_common_sources", f"{ENGINE_ROOT}/renderercommon", ["*.cpp"], []),
    ("renderer_gl_sources", f"{ENGINE_ROOT}/renderer", ["*.cpp"], []),
    ("renderer2_sources", f"{ENGINE_ROOT}/renderer2", ["*.cpp"], ["stringify.cpp"]),
    ("renderer_vk_sources", f"{ENGINE_ROOT}/renderervk", ["*.cpp"], ["bin2hex.cpp"]),
    ("server_sources", f"{ENGINE_ROOT}/server", ["*.cpp"], ["sv_rankings.cpp"]),
    ("unix_core_sources", f"{ENGINE_ROOT}/unix", ["unix_main.cpp", "unix_shared.cpp", "linux_signals.cpp"], []),
    ("sdl_ui_sources", f"{ENGINE_ROOT}/sdl", ["*.cpp"], []),
    ("win32_core_sources", f"{ENGINE_ROOT}/win32", ["win_main.cpp", "win_shared.cpp", "win_syscon.cpp"], []),
    (
        "win32_ui_sources",
        f"{ENGINE_ROOT}/win32",
        ["win_gamma.cpp", "win_glimp.cpp", "win_input.cpp", "win_minimize.cpp", "win_snd.cpp", "win_wndproc.cpp"],
        [],
    ),
    ("win32_qgl_sources", f"{ENGINE_ROOT}/win32", ["win_qgl.cpp"], []),
    ("win32_qvk_sources", f"{ENGINE_ROOT}/win32", ["win_qvk.cpp"], []),
]

FILE_LIST_GROUPS = [
    ("renderer_shared_aux_sources", [f"{ENGINE_ROOT}/qcommon/puff.cpp", f"{ENGINE_ROOT}/qcommon/q_math.cpp", f"{ENGINE_ROOT}/qcommon/q_shared.cpp"]),
]

STRING_LIST_GROUPS = [
    (
        "renderer2_glsl_sources",
        sorted(
            Path(os.path.relpath(path, ROOT)).as_posix()
            for path in (ROOT / "src" / "engine" / "renderer2" / "glsl").glob("*.glsl")
        ),
    ),
]


def resolve_patterns(base: Path, patterns: list[str], excludes: list[str]) -> list[str]:
    paths: set[Path] = set()
    for pattern in patterns:
        paths.update(base.glob(pattern))

    exclude_paths: set[Path] = set()
    for pattern in excludes:
        exclude_paths.update(base.glob(pattern))

    files = [
        Path(os.path.relpath(path, OUT_BASE)).as_posix()
        for path in sorted(paths)
        if path.is_file() and path not in exclude_paths
    ]
    return files


def emit_files_block(name: str, files: list[str]) -> list[str]:
    lines = [f"{name} = files("]
    for path in files:
        lines.append(f"  '{path}',")
    lines.append(")")
    lines.append("")
    return lines


def emit_string_block(name: str, values: list[str]) -> list[str]:
    lines = [f"{name} = ["]
    for value in values:
        lines.append(f"  '{value}',")
    lines.append("]")
    lines.append("")
    return lines


def main() -> None:
    OUT.parent.mkdir(parents=True, exist_ok=True)

    lines = [
        "# This file is generated by scripts/generate_meson_source_lists.py.",
        "# Update the generator when source layout changes, then regenerate.",
        "",
    ]

    for name, rel_dir, patterns, excludes in GROUPS:
        files = resolve_patterns(ROOT / rel_dir, patterns, excludes)
        lines.extend(emit_files_block(name, files))

    for name, files in FILE_LIST_GROUPS:
        rel_files = [Path(os.path.relpath(ROOT / rel_path, OUT_BASE)).as_posix() for rel_path in files]
        lines.extend(emit_files_block(name, rel_files))

    for name, values in STRING_LIST_GROUPS:
        lines.extend(emit_string_block(name, values))

    OUT.write_text("\n".join(lines), encoding="utf-8")


if __name__ == "__main__":
    main()
